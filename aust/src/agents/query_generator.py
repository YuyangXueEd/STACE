    @staticmethod
    def _deduplicate_results(results: Sequence[SearchResult]) -> list[SearchResult]:
        deduped: dict[tuple[str, str], SearchResult] = {}
        for entry in results:
            key = (entry.arxiv_id, entry.section)
            if key not in deduped or entry.similarity_score > deduped[key].similarity_score:
                deduped[key] = entry

        return sorted(
            deduped.values(),
            key=lambda item: item.similarity_score,
            reverse=True,
        )

    def _create_agent(self, model_name: str, *, system_prompt: str) -> ChatAgent:
        """Create ChatAgent for query generation."""
        config_dict = deepcopy(self._model_config)
        config_dict.setdefault("n", 1)
        config = ChatGPTConfig(**config_dict)

        backend = ModelFactory.create(
            model_platform=ModelPlatformType.OPENAI_COMPATIBLE_MODEL,
            model_type=model_name,
            url="https://openrouter.ai/api/v1",
            api_key=os.getenv("OPENROUTER_API_KEY"),
            model_config_dict=config.as_dict(),
        )

        system_message = BaseMessage.make_assistant_message(
            role_name="QueryGenerator",
            content=system_prompt,
        )

        return ChatAgent(system_message=system_message, model=backend)

    def _extract_queries_from_response(
        self, response
    ) -> list[QueryCandidate]:
        """Extract query candidates from structured output or raw content."""
        if not response or not getattr(response, "msgs", None):
            return []

        last_message = response.msgs[-1]
        parsed_output = getattr(last_message, "parsed", None)
        if parsed_output is not None:
            return self._convert_structured_output(parsed_output)

        content = last_message.content if last_message else ""
        return self._parse_query_response(content)

    def _build_prompt(
        self,
        *,
        iteration_number: int,
        task_spec: Optional[Mapping[str, Any]],
        hypothesis: Optional[Hypothesis],
        critic_feedback: Optional[CriticFeedback],
        past_results: Optional[Sequence[dict[str, Any]]],
    ) -> str:
        """Construct user prompt for the LLM."""
        spec_dict = self._normalize_task_spec(task_spec)
        prompt = self._user_prompt_template.format(
            task_spec_section=self._render_task_spec_section(spec_dict),
            hypothesis_section=self._render_hypothesis_section(hypothesis),
            evaluation_section=self._render_evaluation_section(past_results),
            critic_section=self._render_critic_section(critic_feedback),
            max_queries=self.max_queries,
            iteration_number=iteration_number,
        )
        return prompt

    def _parse_query_response(self, response_text: str) -> list[QueryCandidate]:
        """Parse LLM response into query candidates."""
        json_match = re.search(r"```json\s*(\{.*?\})\s*```", response_text, re.DOTALL)
        json_str: Optional[str] = None
        if json_match:
            json_str = json_match.group(1)
        else:
            inline_match = re.search(r"(\{.*\})", response_text, re.DOTALL)
            if inline_match:
                json_str = inline_match.group(1)

        if not json_str:
            raise ValueError("JSON block not found in query generator response.")

        payload = json.loads(json_str)
        query_items = payload.get("queries", [])

        candidates: list[QueryCandidate] = []
        for item in query_items[: self.max_queries]:
            query_text = (item or {}).get("query")
            if not query_text:
                continue
            target_collection = (item or {}).get("target_collection")
            focus_sections = self._coerce_focus_sections((item or {}).get("focus_sections"))
            candidates.append(
                QueryCandidate(
                    query=query_text.strip(),
                    target_collection=str(target_collection).strip() if target_collection else None,
                    focus_sections=focus_sections,
                )
            )

        return candidates

    def _search_queries(
        self,
        queries: Sequence[QueryCandidate],
        task_spec: Optional[Mapping[str, Any]] = None,
    ) -> list[QuerySearchResult]:
        """Execute PaperRAG search for each query."""
        results: list[QuerySearchResult] = []
        spec_dict = self._normalize_task_spec(task_spec)
        default_collection = self._default_collection_for_task(spec_dict)
        model_type_filter = self._infer_model_type_filter(spec_dict)

        for candidate in queries:
            normalized_collection = self._normalize_collection_name(
                candidate.target_collection, default_collection
            )
            focus_sections = self._normalize_focus_sections(candidate.focus_sections)
            # Disable section filtering - search full papers only
            # section_filters = focus_sections or [None]

            task_type_filter = self._collection_to_task_type(normalized_collection)

            aggregated: list[SearchResult] = []
            try:
                # Search without section filter (always search FULL_PAPER)
                rag_results = self.rag.search(
                    query=candidate.query,
                    top_k=self.top_k,
                    section_filter=None,  # Disabled - papers are indexed as FULL_PAPER
                    task_type_filter=task_type_filter,
                    model_type_filter=model_type_filter,
                    similarity_threshold=0.15,  # Lowered to 0.15 for short keyword queries (was 0.5 → 0.3 → 0.15)
                )
                aggregated.extend(rag_results)
            except Exception as exc:
                logger.error("PaperRAG search failed for query '%s': %s", candidate.query, exc)
                aggregated = []

            deduped_results = self._deduplicate_results(aggregated)

            results.append(
                QuerySearchResult(
                    query=candidate.query,
                    target_collection=normalized_collection,
                    focus_sections=[section for section in focus_sections if section],
                    results=deduped_results[: self.top_k],
                )
            )

        return results

    def _flatten_results(
        self, search_results: Sequence[QuerySearchResult]
    ) -> list[dict[str, Any]]:
        """Flatten search results into list of dictionaries for HypothesisContext."""
        flattened: list[dict[str, Any]] = []
        seen_keys: set[tuple[str, str]] = set()

        for entry in search_results:
            for result in entry.results:
                key = (result.arxiv_id, result.section)
                if key in seen_keys:
                    continue
                seen_keys.add(key)
                flattened.append(
                    {
                        "title": result.paper_title,
                        "arxiv_id": result.arxiv_id,
                        "section": result.section,
                        "relevance_score": result.similarity_score,
                        "summary": result.text,
                        "task_type": result.task_type,
                        "attack_level": result.attack_level,
                        "model_type": result.model_type,
                        "source_query": entry.query,
                        "target_collection": entry.target_collection,
                        "focus_sections": entry.focus_sections,
                    }
                )

        return flattened

    def _log_queries(
        self,
        iteration_number: int,
        result: QueryGenerationResult,
        task_spec: Optional[Mapping[str, Any]],
        hypothesis: Optional[Hypothesis],
        critic_feedback: Optional[CriticFeedback],
    ):
        """Persist query generation details to JSON log."""
        timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
        log_payload: dict[str, Any] = {
            "iteration": iteration_number,
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "task_spec": self._normalize_task_spec(task_spec),
            "hypothesis": hypothesis.model_dump() if hypothesis else None,
            "critic_feedback": critic_feedback.model_dump() if critic_feedback else None,
            "result": result.to_log_dict(),
        }

        log_path = self.output_dir / f"queries_iter{iteration_number}_{timestamp}.json"
        try:
            with open(log_path, "w", encoding="utf-8") as handle:
                json.dump(
                    log_payload,
                    handle,
                    indent=2,
                    default=self._json_default,
                )
            logger.info("Logged query generation to %s", log_path)
        except Exception as exc:
            logger.error("Failed to write query log: %s", exc, exc_info=True)

    def _normalize_task_spec(
        self, task_spec: Optional[Mapping[str, Any]]
    ) -> Optional[dict[str, Any]]:
        """Convert TaskSpec object to a plain dictionary if possible."""
        if not task_spec:
            return None

        if isinstance(task_spec, Mapping):
            return {str(key): value for key, value in task_spec.items()}

        if hasattr(task_spec, "model_dump"):
            try:
                return task_spec.model_dump()
            except Exception:
                logger.debug("TaskSpec model_dump() failed", exc_info=True)

        if hasattr(task_spec, "dict"):
            try:
                return task_spec.dict()
            except Exception:
                logger.debug("TaskSpec dict() failed", exc_info=True)

        try:
            return dict(task_spec)
        except Exception:
            logger.warning("Unable to normalize TaskSpec; logging string representation.")
            return {"raw": str(task_spec)}

    def _json_default(self, obj):
        """Fallback serializer for datetimes and other unsupported types."""
        if isinstance(obj, datetime):
            return obj.isoformat()
        if hasattr(obj, "model_dump"):
            return obj.model_dump(mode="json")
        if isinstance(obj, Path):
            return str(obj)
        raise TypeError(f"Object of type {obj.__class__.__name__} is not JSON serializable")

    def _convert_structured_output(
        self, structured_output: Any
    ) -> list[QueryCandidate]:
        """Convert structured output (Pydantic model or dict) into candidates."""
        if structured_output is None:
            return []

        if isinstance(structured_output, BaseModel):
            data = structured_output.model_dump()
        elif isinstance(structured_output, dict):
            data = structured_output
        else:
            logger.warning(
                "Unexpected structured output type from query generator: %s",
                type(structured_output),
            )
            return []

        queries_data = data.get("queries", [])
        candidates: list[QueryCandidate] = []
        for item in queries_data[: self.max_queries]:
            if isinstance(item, BaseModel):
                item_dict = item.model_dump()
            elif isinstance(item, dict):
                item_dict = item
            else:
                logger.debug("Skipping non-dict query item: %s", item)
                continue
            query_text = (item_dict.get("query") or "").strip()
            if not query_text:
                continue
            target_collection = item_dict.get("target_collection")
            focus_sections = self._coerce_focus_sections(item_dict.get("focus_sections"))
            candidates.append(
                QueryCandidate(
                    query=query_text,
                    target_collection=str(target_collection).strip() if target_collection else None,
                    focus_sections=focus_sections,
                )
            )

        return candidates
