    def _normalize_focus_sections(
        self, sections: Optional[Sequence[str]]
    ) -> list[Optional[str]]:
        if not sections:
            return []

        normalized: list[Optional[str]] = []
        seen: set[Optional[str]] = set()
        for raw in sections:
            if raw is None:
                continue
            candidate = str(raw).strip()
            if not candidate:
                continue
            key = candidate.lower().replace(" ", "_").replace("-", "_")
            mapped = SECTION_ALIAS_MAP.get(key)
            if mapped:
                section_value: Optional[str] = mapped
            else:
                upper = candidate.upper().replace(" ", "_").replace("-", "_")
                section_value = upper if upper in KNOWN_SECTIONS else None

            if section_value not in seen:
                seen.add(section_value)
                normalized.append(section_value)

        return normalized

    @staticmethod
    def _coerce_focus_sections(value: Any) -> list[str]:
        if value is None:
            return []
        if isinstance(value, str):
            return [value]
        if isinstance(value, Sequence):
            return [str(item) for item in value if str(item).strip()]
        return []

    def _enforce_keyword_query_style(
        self, candidates: Sequence[QueryCandidate]
    ) -> list[QueryCandidate]:
        """
        Ensure each RAG query is a single keyword or two-word phrase at most.
        """
        normalized: list[QueryCandidate] = []
        seen: set[str] = set()

        for candidate in candidates:
            fragments = self._split_into_keyword_fragments(candidate.query)
            if not fragments:
                continue
            for fragment in fragments:
                key = fragment.lower()
                if key in seen:
                    continue
                seen.add(key)
                normalized.append(
                    QueryCandidate(
                        query=fragment,
                        target_collection=candidate.target_collection,
                        focus_sections=list(candidate.focus_sections),
                    )
                )
                if len(normalized) >= self.max_queries:
                    return normalized

        if not normalized:
            fallback = [
                QueryCandidate(
                    query=candidate.query.strip(),
                    target_collection=candidate.target_collection,
                    focus_sections=list(candidate.focus_sections),
                )
                for candidate in candidates
                if candidate.query.strip()
            ]
            return fallback[: self.max_queries]

        return normalized

    def _split_into_keyword_fragments(self, raw_query: str) -> list[str]:
        if not isinstance(raw_query, str):
            return []

        cleaned = self._strip_parenthetical_blocks(raw_query)
        cleaned = (
            cleaned.replace("“", '"')
            .replace("”", '"')
            .replace("’", "'")
            .replace("–", " ")
            .replace("—", " ")
            .replace("−", " ")
        )

        fragments: list[str] = []
        quote_pattern = re.compile(r"(['\"])(.*?)\1")
        for match in quote_pattern.finditer(cleaned):
            phrase = match.group(2)
            fragments.extend(self._normalize_keyword_phrase(phrase))

        cleaned = quote_pattern.sub(" ", cleaned)
        cleaned = re.sub(r"\b(?:OR|AND|NOT)\b", " ", cleaned, flags=re.IGNORECASE)
        cleaned = re.sub(r"[,\|;:+]", " ", cleaned)
        cleaned = cleaned.replace("/", " ").replace("\\", " ")
        cleaned = re.sub(r"\s+", " ", cleaned).strip()

        if not cleaned:
            return fragments

        for chunk in cleaned.split(" "):
            fragments.extend(self._normalize_keyword_phrase(chunk))

        deduped: list[str] = []
        seen: set[str] = set()
        for fragment in fragments:
            key = fragment.lower()
            if key in seen:
                continue
            seen.add(key)
            deduped.append(fragment)

        return deduped

    @staticmethod
    def _strip_parenthetical_blocks(text: str) -> str:
        if not isinstance(text, str):
            return ""
        previous = None
        current = text
        # Remove nested parentheses iteratively.
        while previous != current:
            previous = current
            current = re.sub(r"\([^()]*\)", " ", current)
        return current

    def _normalize_keyword_phrase(self, phrase: Optional[str]) -> list[str]:
        if not phrase:
            return []
        safe = re.sub(r"[^\w\s\-./+]", " ", phrase)
        safe = re.sub(r"\s+", " ", safe).strip()
        if not safe:
            return []
        if not any(char.isalnum() for char in safe):
            return []

        words = safe.split()
        if len(words) <= self.MAX_KEYWORD_TERMS:
            return [safe]

        return [
            " ".join(words[idx : idx + self.MAX_KEYWORD_TERMS])
            for idx in range(len(words) - (self.MAX_KEYWORD_TERMS - 1))
        ]

    def _default_collection_for_task(
        self, spec_dict: Optional[Mapping[str, Any]]
    ) -> str:
        if not spec_dict:
            return "aust_papers"

        task_type = (spec_dict.get("task_type") or "").lower()
        model_name = (spec_dict.get("model_name") or "").lower()
        user_prompt = (spec_dict.get("user_prompt") or "").lower()
        hint_blob = " ".join([task_type, model_name, user_prompt])

        if any(hint in hint_blob for hint in IMAGE_MODEL_HINTS):
            return "aust_papers_any_to_v"

        if any(hint in hint_blob for hint in TEXT_MODEL_HINTS) or "any_to_t" in hint_blob:
            return "aust_papers_any_to_t"

        return "aust_papers"

    def _normalize_collection_name(self, collection: Optional[str], fallback: str) -> str:
        if isinstance(collection, str) and collection.strip():
            candidate = collection.strip()
            if candidate in COLLECTION_TASK_TYPE_MAP:
                return candidate
            lowered = candidate.lower()
            for key in COLLECTION_TASK_TYPE_MAP:
                if lowered == key.lower():
                    return key
        return fallback

    @staticmethod
    def _collection_to_task_type(collection_name: Optional[str]) -> Optional[str]:
        if not collection_name:
            return None
        return COLLECTION_TASK_TYPE_MAP.get(collection_name)

