@dataclass
class QueryCandidate:
    """Single query candidate generated by the agent."""

    query: str
    target_collection: Optional[str] = None
    focus_sections: list[str] = field(default_factory=list)


@dataclass
class QuerySearchResult:
    """Query paired with its PaperRAG search results."""

    query: str
    target_collection: Optional[str]
    results: list[SearchResult] = field(default_factory=list)
    focus_sections: list[str] = field(default_factory=list)


@dataclass
class QueryGenerationResult:
    """Aggregate result from the query generator."""

    search_results: list[QuerySearchResult]
    retrieved_papers: list[dict[str, Any]]

    def to_log_dict(self) -> dict[str, Any]:
        """Convert to JSON-serializable dictionary for logging."""
        return {
            "search_results": [
                {
                    "query": entry.query,
                    "target_collection": entry.target_collection,
                    "focus_sections": entry.focus_sections,
                    "results": [
                        {
                            "arxiv_id": res.arxiv_id,
                            "section": res.section,
                            "text": res.text,
                            "similarity_score": res.similarity_score,
                            "task_type": res.task_type,
                            "attack_level": res.attack_level,
                            "model_type": res.model_type,
                            "paper_title": res.paper_title,
                            "card_path": res.card_path,
                        }
                        for res in entry.results
                    ],
                }
                for entry in self.search_results
            ],
            "retrieved_papers": self.retrieved_papers,
        }


class QueryItemModel(BaseModel):
    """Structured output schema for a single query item."""

    query: str = Field(..., description="Query string for the RAG search")
    target_collection: Optional[str] = Field(
        default=None,
        description="Preferred Qdrant collection (e.g., aust_papers_any_to_v)",
    )
    focus_sections: list[str] = Field(
        default_factory=list,
        description="List of paper card sections to prioritize",
    )


class QueryResponseModel(BaseModel):
    """Structured output schema for the query generator."""

    queries: list[QueryItemModel] = Field(
        default_factory=list,
        description="List of proposed RAG queries",
    )


class QueryGeneratorAgent:
    """LLM-assisted query generator for PaperRAG searches."""

    DEFAULT_MODEL = _DEFAULT_QUERY_MODEL
    DEFAULT_MAX_QUERIES = 3
    DEFAULT_TOP_K = 5
    MAX_KEYWORD_TERMS = 2

    def __init__(
        self,
        rag: PaperRAG,
        model_name: Optional[str] = None,
        max_queries: int = DEFAULT_MAX_QUERIES,
        top_k: int = DEFAULT_TOP_K,
        output_dir: Optional[Path] = None,
    ):
        """
        Initialize Query Generator agent.

        Args:
            rag: PaperRAG instance for semantic search
            model_name: OpenRouter-compatible model name
            max_queries: Maximum number of queries to request
            top_k: Number of PaperRAG results per query
            output_dir: Directory for query logs
        """
        self.rag = rag
        self._model_config = deepcopy(_DEFAULT_QUERY_MODEL_CONFIG)
        self.model_name = model_name or self.DEFAULT_MODEL
        self.max_queries = max(1, max_queries)
        self.top_k = max(1, top_k)
        self.output_dir = output_dir or Path("aust/outputs/queries")
        self.output_dir.mkdir(parents=True, exist_ok=True)

        self._prompt_config = self._load_prompt_config()
        system_prompt = self._prompt_config.get("system_prompt")
        if not isinstance(system_prompt, str) or not system_prompt.strip():
            raise ValueError(
                f"Query generator system prompt missing or invalid at {QUERY_PROMPT_PATH}"
            )
        self._user_prompt_template = self._prompt_config.get("user_prompt_template")
        if not isinstance(self._user_prompt_template, str) or not self._user_prompt_template.strip():
            raise ValueError(
                f"Query generator user prompt template missing or invalid at {QUERY_PROMPT_PATH}"
            )

        logger.info(
            "Initializing QueryGeneratorAgent with model=%s, max_queries=%s, top_k=%s",
            self.model_name,
            self.max_queries,
            self.top_k,
        )

        self._agent = self._create_agent(self.model_name, system_prompt=system_prompt.strip())
        self._context_section_chars = None
        self._response_model = QueryResponseModel

    def generate(
        self,
        *,
        iteration_number: int,
        task_spec: Optional[Mapping[str, Any]] = None,
        hypothesis: Optional[Hypothesis] = None,
        critic_feedback: Optional[CriticFeedback] = None,
        past_results: Optional[Sequence[dict[str, Any]]] = None,
    ) -> QueryGenerationResult:
        """
        Generate queries and execute PaperRAG search.

        Args:
            iteration_number: Current inner loop iteration
            task_spec: Structured TaskSpec dictionary (if available)
            hypothesis: Latest hypothesis to ground search
            critic_feedback: Feedback from critic agent
            past_results: Summaries of previous iterations

        Returns:
            QueryGenerationResult object containing queries and retrieved papers
        """
        prompt = self._build_prompt(
            iteration_number=iteration_number,
            task_spec=task_spec,
            hypothesis=hypothesis,
            critic_feedback=critic_feedback,
            past_results=past_results,
        )

        user_message = BaseMessage.make_user_message(
            role_name="Orchestrator", content=prompt
        )

        queries: list[QueryCandidate] = []
        try:
            logger.debug(
                "Invoking QueryGeneratorAgent LLM for iteration %s",
                iteration_number,
            )
            response = self._agent.step(
                user_message, response_format=self._response_model
            )
            raw_queries = self._extract_queries_from_response(response)
            if not raw_queries:
                raise RuntimeError("Query generator returned no queries.")
            queries = self._enforce_keyword_query_style(raw_queries)
            if not queries:
                raise RuntimeError("Query normalization produced no usable queries.")
        except Exception as exc:
            logger.error("Query generation failed: %s", exc, exc_info=True)
            raise
        finally:
            self._agent.reset()

        search_results = self._search_queries(queries, task_spec=task_spec)
        retrieved_papers = self._flatten_results(search_results)

        result = QueryGenerationResult(
            search_results=search_results,
            retrieved_papers=retrieved_papers,
        )

        self._log_queries(iteration_number, result, task_spec, hypothesis, critic_feedback)
        return result

    def _load_prompt_config(self) -> dict[str, Any]:
        """Load YAML prompt configuration for the query agent."""
        if not QUERY_PROMPT_PATH.exists():
            raise FileNotFoundError(f"Query prompt configuration not found at {QUERY_PROMPT_PATH}")

        with QUERY_PROMPT_PATH.open("r", encoding="utf-8") as handle:
            payload = yaml.safe_load(handle) or {}

        if not isinstance(payload, dict):
            raise ValueError(f"Query prompt configuration {QUERY_PROMPT_PATH} must be a YAML mapping")

        return payload

    def _render_task_spec_section(self, spec_dict: Optional[Mapping[str, Any]]) -> str:
        if not spec_dict:
            return "- No TaskSpec provided."

        lines: list[str] = []
        fields = [
            ("task_type", "Task Type"),
            ("model_name", "Model Name"),
            ("model_version", "Model Version"),
            ("base_model_path", "Base Model Path"),
            ("unlearned_model_path", "Unlearned Model Path"),
            ("unlearned_target", "Unlearned Target"),
            ("unlearning_method", "Unlearning Method"),
        ]
        for key, label in fields:
            value = spec_dict.get(key)
            if value:
                lines.append(f"- {label}: {value}")

        user_prompt = (spec_dict.get("user_prompt") or "").strip()
        if user_prompt:
            indented_prompt = textwrap.indent(user_prompt, "  ")
            lines.append("- User Prompt:\n" + indented_prompt)

        metadata = spec_dict.get("metadata")
        if isinstance(metadata, dict) and metadata.get("field_sources"):
            sources = metadata["field_sources"]
            if isinstance(sources, dict):
                origin_info = ", ".join(f"{k}: {v}" for k, v in sources.items())
                if origin_info:
                    lines.append(f"- Field sources: {origin_info}")

        return "\n".join(lines) if lines else "- TaskSpec metadata present but fields were empty."

    def _render_hypothesis_section(self, hypothesis: Optional[Hypothesis]) -> str:
        if not hypothesis:
            return "- No hypothesis generated yet. Base queries on TaskSpec context."

        description = (hypothesis.description or "").strip()
        if description:
            return f"- Description: {description}"
        return "- Hypothesis description unavailable."

    def _render_evaluation_section(self, past_results: Optional[Sequence[dict[str, Any]]]) -> str:
        if not past_results:
            return "- No evaluation results recorded yet."

        lines = []
        for summary in past_results:
            iteration = summary.get("iteration") or summary.get("iteration_number") or "?"
            outcome = summary.get("outcome") or "Outcome not recorded."
            confidence = summary.get("vulnerability_confidence")
            confidence_text = (
                f" (vulnerability confidence={float(confidence):.2f})"
                if isinstance(confidence, (int, float))
                else ""
            )

            lines.append(f"- Iteration {iteration}: {outcome}{confidence_text}")

        return "\n".join(lines)

